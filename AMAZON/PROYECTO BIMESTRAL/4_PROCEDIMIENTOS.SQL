CREATE OR REPLACE PROCEDURE SP_TRIMESTRAL(P_ID_TIPO NUMBER)
IS 
V_PAGO NUMBER(6);
BEGIN
	SELECT COSTO 
	INTO V_PAGO
	FROM TIPO
	WHERE ID_TIPO = P_ID_TIPO;	
	V_PAGO := V_PAGO * 3;
    DBMS_OUTPUT.PUT_LINE('PAGO TRIMESTRAL ES DE:' || V_PAGO); 
END;

EXECUTE SP_TRIMESTRAL(1);

