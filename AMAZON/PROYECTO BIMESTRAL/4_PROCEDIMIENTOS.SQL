CREATE OR REPLACE PROCEDURE SP_CATEGORIA(P_ID_CATEGORIA NUMBER, P_CATALOGO VARCHAR)
IS
BEGIN
	INSERT INTO CATEGORIA VALUES(P_ID_CATEGORIA, P_CATALOGO);
END SP_CATEGORIA;

EXECUTE SP_CATEGORIA(129,'UNISEX');

---------------------------------------------------------------------------------------

CREATE OR REPLACE PROCEDURE SP_BAJA(P_ID_CATEGORIA NUMBER)
IS
BEGIN
	DELETE FROM CATEGORIA WHERE ID_CATEGORIA = P_ID_CATEGORIA;
END SP_BAJA;

EXECUTE SP_BAJA(129);

