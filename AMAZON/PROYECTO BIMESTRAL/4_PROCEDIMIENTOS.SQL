CREATE OR REPLACE PROCEDURE SP_ESTATUS(P_ID_ENVIOS NUMBER)
IS 
V_ESTATUS VARCHAR(15);
BEGIN
	SELECT ESTATUS
	INTO V_ESTATUS
	FROM ENVIOS
	WHERE ID_ENVIOS = P_ID_ENVIOS;	
    DBMS_OUTPUT.PUT_LINE('EL ESTATUS DEL ID DE INVIO ES:' || V_ESTATUS); 
END;

EXECUTE SP_ESTATUS(1000000);