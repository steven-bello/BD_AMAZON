CREATE OR REPLACE PROCEDURE SP_DATOS_CLIENTE(P_ID_CLIENTE NUMBER)
AS
V_ID_CLIENTE NUMBER(5);
V_NOMBREU VARCHAR(50);
V_EDAD NUMBER(2);
V_GENERO CHAR(10);
V_TELEFONO NUMBER(10);
V_CORREO VARCHAR(40);

BEGIN
SELECT ID_CLIENTE, NOMBREU, EDAD, GENERO, TELEFONO, CORREO
INTO V_ID_CLIENTE, V_NOMBREU, V_EDAD, V_GENERO, V_TELEFONO, V_CORREO
FROM USUARIO
WHERE ID_CLIENTE = P_ID_CLIENTE;
DBMS_OUTPUT.PUT_LINE('//--Datos del cliente--//');
DBMS_OUTPUT.PUT_LINE('ID: '|| V_ID_CLIENTE);
DBMS_OUTPUT.PUT_LINE('Nombre: '|| V_NOMBREU);
DBMS_OUTPUT.PUT_LINE('Edad: '|| V_EDAD);
DBMS_OUTPUT.PUT_LINE('Genero: '|| V_GENERO);
DBMS_OUTPUT.PUT_LINE('Telefono: '|| V_TELEFONO);
DBMS_OUTPUT.PUT_LINE('Correo: '|| V_CORREO);
END;

EXEC SP_DATOS_CLIENTE(4);

